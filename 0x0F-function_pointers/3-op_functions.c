#include "3-calc.h"

/**
 * op_add - returns sum of two int
 * @a: first int
 * @b: second int
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus a and b
 * @a: first int
 * @b: second int
 * Return: a- b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a and b
 * @a: first int
 * @b: second int
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a and b
 * @a: first int
 * @b: second int
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of a and b
 * @a: first int
 * @b: second int
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
